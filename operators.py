#arthematic operators
a = 5
b = 2
print(a + b)    
print(a - b)    
print(a * b)    
print(a / b)    
print(a % b)    
print(a ** b)   

#relational operators
a = 50
b = 20
print(a == b)   
print(a != b)   
print(a >= b)   
print(a > b)    
print(a <= b)   
print(a < b)    

#assignment operators
a = 50
b = 20
print(a == b)   
print(a != b)   
print(a >= b)   
print(a > b)    
print(a <= b)   
print(a < b)    

#logical operators
a = 10
b = 5
print(a > 5 and b < 10)
print(a > 5 or b > 10)
print(not(a > 5))
